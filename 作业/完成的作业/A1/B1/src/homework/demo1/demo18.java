//编写Java程序，在控制台上打印出九九乘法表
public class demo18 {
    public static void main(String[]args){

        for (int a=1;a<=9;a++){
            for(int b=1;b<=a;b++) {
                System.out.print(a+"*"+b+"="+a*b+"  ");


            }System.out.println();

        }



    }


}
